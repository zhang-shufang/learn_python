locations = ['xinjiang', 'xizang', 'neimenggu', 'hainan', 'taiwan']

print("Original order:")
print(locations)

print("\nAlphabetical:")
print(sorted(locations))

print("Original order:")
print(locations)

print("\nReverse alphabetical:")
print(sorted(locations, reverse=True))

print("Original order:")
print(locations)

print("\nReversed:")
locations.reverse()
print(locations)

print("Original order:")
locations.reverse()
print(locations)

print("\nAlphabetical")
locations.sort()
print(locations)

print("\nReverse alphabetical")
locations.reverse()
print(locations)
